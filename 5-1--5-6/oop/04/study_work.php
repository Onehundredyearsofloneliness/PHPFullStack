三 基类（父类）方法的重写和基类（父类）方法的访问
	重写: 子类和父类方法重名，子类方法重写了父类方法。
	父类方法的访问， 父类类名::方法     parent::方法	
	
	应用:
		开发中 如果 已经存在类似功能的类，但是不完善，用继承补充 不完善的类。
		
		
		
		
		
		
		
		
		
		
		
			
		
		
		
		
		
		
			
			
			
			
			
			
			
			
			
			




